package kcl.qutong.plagiarism.entity.SmithWaterman;
/**
 * represent the point in score matrix 
 * @author qutong
 *
 */
public class point {
int x;//x-coordinate index of x
int y;
int value;
public int getX() {
	return x;
}
public void setX(int x) {
	this.x = x;
}
public int getY() {
	return y;
}
public void setY(int y) {
	this.y = y;
}
public int getValue() {
	return value;
}
public void setValue(int value) {
	this.value = value;
}
}
